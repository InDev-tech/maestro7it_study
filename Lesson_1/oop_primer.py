# 1. Определяем класс (чертеж)
class Cat:
    # Конструктор (инициализирует объект)
    def __init__(self, name, age):
        # Свойства (атрибуты) объекта
        self.name = name  # публичное свойство
        self.age = age    # публичное свойство
    
    # Метод (действие)
    def meow(self):
        return f"{self.name} говорит: Мяу"

# 2. Создаем объекты (экземпляры) класса
cat1 = Cat("Барсик", 3)
cat2 = Cat("Мурка", 2)

# 3. Используем объекты
print(cat1.name)     # Вывод: Барсик
print(cat2.meow())   # Вывод: Мурка говорит: Мяу
