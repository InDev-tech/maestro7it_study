def insertion_sort(arr):
    """
    Сортировка вставками.
    
    Алгоритм строит итоговый отсортированный массив по одному элементу за раз.
    Берет элемент из неотсортированной части и вставляет его в правильную позицию
    в уже отсортированной части, сдвигая остальные элементы.
    
    Эффективен на небольших массивах или почти отсортированных данных.
    
    Сложность:
        - Время: O(n^2) в худшем и среднем случае, O(n) в лучшем.
        - Память: O(1) (сортировка на месте).
    
    Аргументы:
        arr (list): Список элементов, поддерживающих сравнение.

    Возвращает:
        list: Отсортированный список (исходный список изменяется).
    """
    for i in range(1, len(arr)):
        key = arr[i]  # Элемент, который нужно вставить
        j = i - 1
        # Сдвигаем элементы, которые больше key, на одну позицию вперед
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        # Вставляем key на освободившееся место
        arr[j + 1] = key
    return arr

# Пример использования и проверки
if __name__ == "__main__":
    test_array = [64, 34, 25, 12, 22, 11, 90]
    
    print(f"Исходный массив: {test_array}")
    print(f"Вставками:        {insertion_sort(test_array.copy())}")