def bubble_sort(arr):
    """
    Пузырьковая сортировка.
    
    Алгоритм проходит по массиву, сравнивая соседние элементы и меняя их местами,
    если они стоят в неправильном порядке. Процесс повторяется до тех пор, 
    пока массив не будет полностью отсортирован. Наибольший элемент "всплывает" 
    в конец массива на каждой итерации.
    
    Сложность:
        - Время: O(n^2) в худшем и среднем случае, O(n) в лучшем (уже отсортированный массив).
        - Память: O(1) (сортировка на месте).
    
    Аргументы:
        arr (list): Список элементов, поддерживающих сравнение (числа, строки).

    Возвращает:
        list: Отсортированный список (исходный список изменяется).
    """
    n = len(arr)
    # Проход по всем элементам массива
    for i in range(n):
        # Флаг для оптимизации: если замен не было, массив уже отсортирован
        swapped = False
        # Последние i элементов уже на своих местах
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        if not swapped:
            break
    return arr


def insertion_sort(arr):
    """
    Сортировка вставками.
    
    Алгоритм строит итоговый отсортированный массив по одному элементу за раз.
    Берет элемент из неотсортированной части и вставляет его в правильную позицию
    в уже отсортированной части, сдвигая остальные элементы.
    
    Эффективен на небольших массивах или почти отсортированных данных.
    
    Сложность:
        - Время: O(n^2) в худшем и среднем случае, O(n) в лучшем.
        - Память: O(1) (сортировка на месте).
    
    Аргументы:
        arr (list): Список элементов, поддерживающих сравнение.

    Возвращает:
        list: Отсортированный список (исходный список изменяется).
    """
    for i in range(1, len(arr)):
        key = arr[i]  # Элемент, который нужно вставить
        j = i - 1
        # Сдвигаем элементы, которые больше key, на одну позицию вперед
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        # Вставляем key на освободившееся место
        arr[j + 1] = key
    return arr


def selection_sort(arr):
    """
    Сортировка выбором.
    
    Алгоритм делит массив на отсортированную (слева) и неотсортированную (справа) части.
    На каждом шаге ищет минимальный элемент в неотсортированной части и меняет его
    местами с первым элементом неотсортированной части.
    
    Сложность:
        - Время: O(n^2) во всех случаях (всегда делает n^2 сравнений).
        - Память: O(1) (сортировка на месте).
    
    Аргументы:
        arr (list): Список элементов, поддерживающих сравнение.

    Возвращает:
        list: Отсортированный список (исходный список изменяется).
    """
    n = len(arr)
    for i in range(n):
        # Ищем индекс минимального элемента в оставшейся части
        min_idx = i
        for j in range(i + 1, n):
            if arr[j] < arr[min_idx]:
                min_idx = j
        # Меняем местами найденный минимум с текущим элементом
        arr[i], arr[min_idx] = arr[min_idx], arr[i]
    return arr


def merge_sort(arr):
    """
    Сортировка слиянием.
    
    Рекурсивный алгоритм, работающий по принципу "разделяй и властвуй".
    Массив рекурсивно делится пополам, пока размер части не станет равен 1,
    затем отсортированные части сливаются воедино.
    
    Сложность:
        - Время: O(n log n) во всех случаях (гарантированно).
        - Память: O(n) (требуется дополнительная память для слияния).
    
    Аргументы:
        arr (list): Список элементов, поддерживающих сравнение.

    Возвращает:
        list: Новый отсортированный список (исходный список не изменяется).
    """
    if len(arr) <= 1:
        return arr

    # Разделение
    mid = len(arr) // 2
    left_half = merge_sort(arr[:mid])
    right_half = merge_sort(arr[mid:])

    # Слияние
    return _merge(left_half, right_half)


def _merge(left, right):
    """Вспомогательная функция для слияния двух отсортированных списков."""
    result = []
    i = j = 0
    
    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
            
    # Добавляем оставшиеся элементы
    result.extend(left[i:])
    result.extend(right[j:])
    return result


def quick_sort(arr, low=0, high=None):
    """
    Быстрая сортировка (схема Хоара).
    
    Алгоритм "разделяй и властвуй". Выбирает опорный элемент (pivot) и 
    перераспределяет элементы так, что меньшие оказываются слева, а большие справа.
    Затем рекурсивно применяется к подмассивам.
    
    Сложность:
        - Время: O(n log n) в среднем, O(n^2) в худшем (при плохом выборе опорного).
        - Память: O(log n) (стек вызовов).
    
    Аргументы:
        arr (list): Список элементов.
        low (int): Начальный индекс сортируемой части.
        high (int): Конечный индекс сортируемой части.

    Возвращает:
        list: Отсортированный список (исходный список изменяется).
    """
    if high is None:
        high = len(arr) - 1
        
    if low < high:
        # Разделение
        pi = _partition(arr, low, high)
        # Рекурсивная сортировка левой и правой частей
        quick_sort(arr, low, pi - 1)
        quick_sort(arr, pi + 1, high)
    return arr


def _partition(arr, low, high):
    """Разделение массива относительно опорного элемента."""
    pivot = arr[high]  # Опорный элемент - последний
    i = low - 1        # Индекс меньшего элемента
    
    for j in range(low, high):
        if arr[j] <= pivot:
            i += 1
            arr[i], arr[j] = arr[j], arr[i]
            
    arr[i + 1], arr[high] = arr[high], arr[i + 1]
    return i + 1


def counting_sort(arr):
    """
    Сортировка подсчетом.
    
    Несравнивающий алгоритм сортировки. Подсчитывает количество вхождений 
    каждого уникального элемента, затем восстанавливает отсортированный массив.
    
    Подходит только для целых чисел (или приводимых к целым) в известном диапазоне.
    
    Сложность:
        - Время: O(n + k), где k - размер диапазона значений.
        - Память: O(k).
    
    Аргументы:
        arr (list): Список целых чисел.

    Возвращает:
        list: Новый отсортированный список.
    
    Исключения:
        ValueError: Если массив пуст.
    """
    if not arr:
        return []
        
    max_val = max(arr)
    min_val = min(arr)
    range_of_values = max_val - min_val + 1
    
    # Массив для подсчета
    count = [0] * range_of_values
    
    # Подсчет элементов
    for num in arr:
        count[num - min_val] += 1
        
    # Восстановление отсортированного массива
    sorted_arr = []
    for i in range(range_of_values):
        sorted_arr.extend([i + min_val] * count[i])
        
    return sorted_arr


