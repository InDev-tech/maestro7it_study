"""
1. Сложность алгоритмов (Big O notation)
Это способ измерения того, как быстро работает алгоритм (время выполнения) или сколько памяти он потребляет (memory complexity) при увеличении входных данных.

Зачем это нужно?
Чтобы понимать, "упадет" ли твой код, если на вход вместо 10 элементов придет 10 миллионов.

Основные виды сложности (от лучшей к худшей):
O(1) — Константная: Время выполнения не зависит от размера данных.
       Пример: получение элемента из списка по индексу (arr[5]).
O(log n) — Логарифмическая: Алгоритм каждый раз отсекает половину данных.
           Пример: бинарный поиск.
O(n) — Линейная: Время выполнения пропорционально количеству данных.
       Пример: простой поиск в неотсортированном списке (нужно проверить каждый элемент).
O(n log n) — Линейно-логарифмическая: Стандарт для хороших сортировок (например, list.sort() в Python, Timsort).
O(n²) — Квадратичная: Часто встречается при вложенных циклах. С такими данными уже тяжело работать. 
        Пример: пузырьковая сортировка.
"""

"""
2. Шаблонные алгоритмы (Паттерны)
Это "джентельменский набор" подходов, которые часто комбинируются для решения конкретных задач.

А. Бинарный поиск (Binary Search)
Условие применения: Массив отсортирован.
Суть: Берем середину, сравниваем с искомым, отбрасываем половину массива.
Сложность: O(log n)
"""

def binary_search(arr, target):
    """
    Возвращает индекс target в отсортированном массиве arr или -1, если не найден.
    """
    left, right = 0, len(arr) - 1

    while left <= right:
        mid = (left + right) // 2 # Индекс середины
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            left = mid + 1 # Ищем справа
        else:
            right = mid - 1 # Ищем слева
    return -1

# Пример
sorted_list = [2, 5, 8, 12, 16, 23, 38, 56]
print(binary_search(sorted_list, 23)) # Вывод: 5
print(binary_search(sorted_list, 1))  # Вывод: -1
