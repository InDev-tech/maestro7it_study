def is_square(n):
    """
    Проверяет, является ли целое число полным квадратом.

    Параметры:
    -----------
    n : int
        Целое число для проверки. Может быть отрицательным.

    Возвращаемое значение:
    ----------------------
    bool
        True, если n является квадратом целого числа, иначе False.

    Примеры использования:
    ----------------------
    >>> is_square(-1)
    False
    >>> is_square(0)
    True
    >>> is_square(25)
    True
    >>> is_square(26)
    False

    Примечания:
    -----------
    Для отрицательных чисел функция сразу возвращает False,
    так как квадрат целого числа не может быть отрицательным.
    """
    if n < 0:
        return False
    # Извлекаем квадратный корень и проверяем, является ли он целым числом
    return (n ** 0.5).is_integer()