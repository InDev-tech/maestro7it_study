def solution(string: str) -> str:
    """
    Возвращает перевернутую (обратную) строку.

    Параметры:
    -----------
    string : str
        Исходная строка для переворота. Может быть пустой.

    Возвращаемое значение:
    ----------------------
    str
        Строка с символами в обратном порядке.

    Примеры использования:
    ----------------------
    >>> solution('world')
    'dlrow'
    >>> solution('hello')
    'olleh'
    >>> solution('')
    ''
    >>> solution('a')
    'a'

    Примечания:
    -----------
    Используется срез строки с шагом -1 (string[::-1]) — наиболее эффективный
    и "питонический" способ инвертирования последовательности.
    """
    return string[::-1]

print(solution('rgoh'))